print("Vamos a calcular el área de un círculo")

def area_circulo():
    import math
    r=int(input("Introduce un radio:"))
    pi= math.pi
    AREA=pi*(r^2)
    print("Siendo la fórmula: pi*(r^2), el resultado del área, con radio {} es {}".format(r, AREA))
area_circulo()


